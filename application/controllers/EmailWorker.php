        $result = $query->result();

        $emails = array_column($result, 'email'); // extract email list

        $hotel_name = isset($hotel->hotel_name) ? $hotel->hotel_name : 'N/A';
        $department_name = isset($department->department_name) ? $department->department_name : 'N/A';



