        $level_color = '#e67e22';

        switch ($level) {
            case 1:
                $level_text = 'Level 1 Escalation – Follow-up Pending';
                break;

            case 2:
                $level_text = 'Level 2 Escalation – Immediate Attention Required';
                $level_color = '#e74c3c';
                break;

            case 3:
                $level_text = 'Level 3 Escalation – SLA Breach';
                $level_color = '#fa0a0a';
                break;
        }

        return '
    <div style="font-family: Arial, sans-serif; color: #333; max-width: 600px; margin: 0 auto; border: 1px solid #ddd; padding: 20px; background-color: #f9f9f9;">
        <h2 style="color: ' . $level_color . ';">' . $level_text . '</h2>

        <p>The following lead has not been attended within the defined SLA:</p>

        <table style="width: 100%; border-collapse: collapse; margin-top: 15px;">
            <tr><td><strong>Name</strong></td><td>' . htmlspecialchars($leadData->user_name ?? '') . '</td></tr>
            <tr><td><strong>Phone</strong></td><td>' . htmlspecialchars($leadData->phone_number ?? '') . '</td></tr>
            <tr><td><strong>Email</strong></td><td>' . htmlspecialchars($leadData->email ?? '') . '</td></tr>
            <tr><td><strong>Query</strong></td><td>' . nl2br(htmlspecialchars($leadData->query ?? '')) . '</td></tr>
            <tr><td><strong>Property</strong></td><td>' . htmlspecialchars($leadData->hotel_name) . '</td></tr>
            <tr><td><strong>Department</strong></td><td>' . htmlspecialchars($leadData->department_name) . '</td></tr>
            <tr><td><strong>Source</strong></td><td>' . htmlspecialchars($leadData->user_channel ?? '') . '</td></tr>
            <tr><td><strong>Lead Created At</strong></td>
                <td>' . date('d M Y, h:i A', strtotime($leadData->created_at ?? date('Y-m-d H:i:s'))) . '</td>
            </tr>
        </table>

        <p style="margin-top: 20px; font-size: 13px; color: #888;">
            This is an automated escalation email generated by the Lead Management System.
        </p>
    </div>';
    }


    private function getEscalationEmails($lead, $level)
    {
        $this->db->select('staff_members.email');
        $this->db->from('staff_members');
        $this->db->join(
            'staff_hotel_department_mapping',
            'staff_members.id = staff_hotel_department_mapping.staff_id'
        );
        $this->db->where('staff_hotel_department_mapping.hotel_id', $lead->property);
        $this->db->where('staff_hotel_department_mapping.department_id', $lead->type);
        $this->db->where('staff_hotel_department_mapping.escalation_level <=', $level);

        return array_column($this->db->get()->result(), 'email');
    }
