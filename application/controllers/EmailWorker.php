        if ($IsvaluableGuest) {
            $subject = 'New Lead From Valuable Guest: ' . ($leadData->user_name ?? 'Unknown') . ' for ' . ($department_name ?? 'Unknown') . ' - ' . date('d M Y', strtotime($leadData->created_at ?? date('Y-m-d')));
