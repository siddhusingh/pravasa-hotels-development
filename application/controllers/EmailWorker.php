    public function sendEscalationL3($lead_id = null)
    {
        if (!$lead_id) return;

        $this->load->model(['Comman_model', 'Mail_model']);

        $lead = $this->LeadModel->get_lead_with_property_and_department($lead_id);


        $emails = [''];

        $this->db->select('staff_members.email');
        $this->db->from('staff_members');
        $this->db->join('staff_hotel_department_mapping', 'staff_members.id = staff_hotel_department_mapping.staff_id');
        $this->db->where('staff_hotel_department_mapping.hotel_id', $lead->property);
        $this->db->where('staff_hotel_department_mapping.department_id', $lead->type);
        $query = $this->db->get();
        $result = $query->result();

        $cc_emails = array_column($result, 'email'); // extract email list



        $extra_email = $lead->hotel_admin_email;

        $cc_emails[] = $extra_email;

        // Remove duplicates
        $cc_emails = array_values(array_unique($cc_emails));



        $subject = "Level 3 Escalation: SLA Breach";
        $message = $this->buildEscalationEmailBody($lead, 3);

        $this->Mail_model->sendMailSMTP_uv('', $emails, $subject, $message, $cc_emails);
    }


    private function buildEscalationEmailBody($leadData, $level)
